#include <stdio.h>

int main() {
    // 穷举所有可能的购买方案
    for (int y = 1; y <= 40; y++) { // y为西瓜的数量，最多购买40个西瓜
        for (int z = 1; z <= 100 - y; z++) { // z为梨的数量，最多购买100-y个梨
            int x = 100 - y - z; // 苹果的数量，剩余的数量由苹果决定
            // 判断是否满足总价为40元
            if (0.4 * x + 4 * y + 0.2 * z == 40) {
                printf("苹果：%d 个, 西瓜：%d 个, 梨：%d 个\n", x, y, z);
            }
        }
    }

    return 0;
}
